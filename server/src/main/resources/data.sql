DROP TABLE IF EXISTS cards;

CREATE TABLE cards (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  number VARCHAR,
  balance DECIMAL,
  currency VARCHAR,
  pin VARCHAR NOT NULL,
  start_date DATE,
  end_date DATE
);

